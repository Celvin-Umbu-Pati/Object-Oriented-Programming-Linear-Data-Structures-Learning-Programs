    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed

        if (selectedMenuId == -1) {
            JOptionPane.showMessageDialog(this, "Pilih menu dulu Yaaa!");
            return;
        }

        String jumlahText = txtJumlah.getText().trim();
        int jumlah;

        try {
            jumlah = Integer.parseInt(jumlahText);
            if (jumlah <= 0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Maaf Jumlah harus angka > 0!");
            return;
        }

        // untuk ambil detail menu dari database
        Menu menu = MenuDAO.getMenuById(selectedMenuId);

        // Tambahkan ke keranjang (ArrayList)
        orderList.add(new OrderItem(menu, jumlah));

        // Refresh tabel order
        refreshOrderTable();

        // Bersihkan form
        txtNama.setText("");
        txtJumlah.setText("");
        selectedMenuId = -1;

    }//GEN-LAST:event_btnTambahActionPerformed

