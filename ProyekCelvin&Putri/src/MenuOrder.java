    private void loadMenuTable() {

        DefaultTableModel model = new DefaultTableModel(
                new String[]{"ID", "NAMA", "HARGA", "JENIS"}, 0
        );

        List<Menu> menus = MenuDAO.getAllMenu();

        for (Menu m : menus) {
            model.addRow(new Object[]{
                m.getId(),
                m.getNama(),
                m.getHarga(),
                m.getJenis()
            });
        }

        tblMenu.setModel(model);
    }

    private void hitungTotalHarga() {
        double total = 0;
        for (OrderItem oi : orderList) {
            total += oi.getSubtotal();
        }
        txtTotal.setText(String.valueOf(total));
    }

    private void refreshOrderTable() {

        DefaultTableModel model = new DefaultTableModel(
                new String[]{"NAMA", "HARGA", "JUMLAH", "SUBTOTAL"}, 0
        );

        double total = 0;

        for (OrderItem oi : orderList) {
            model.addRow(new Object[]{
                oi.getMenu().getNama(),
                oi.getMenu().getHarga(),
                oi.getJumlah(),
                oi.getSubtotal()
            });

            total += oi.getSubtotal();
        }

        tblOrder.setModel(model);
        txtTotal.setText(String.valueOf(total));
    }

