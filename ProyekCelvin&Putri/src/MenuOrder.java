    private void tblMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMenuMouseClicked

        int row = tblMenu.getSelectedRow();
        if (row >= 0) {

            String nama = tblMenu.getValueAt(row, 1).toString();
            txtNama.setText(nama);

            selectedMenuId = Integer.parseInt(
                    tblMenu.getValueAt(row, 0).toString()
            );
        }

    }//GEN-LAST:event_tblMenuMouseClicked

    private void tblOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrderMouseClicked

        selectedOrderIndex = tblOrder.getSelectedRow();
        if (selectedOrderIndex == -1) {
            return;
        }

        OrderItem oi = orderList.get(selectedOrderIndex);

        txtNama.setText(oi.getMenu().getNama());
        txtJumlah.setText(String.valueOf(oi.getJumlah()));

        // simpan juga ID menu supaya tombol tambah bisa update
        selectedMenuId = oi.getMenu().getId();


    }//GEN-LAST:event_tblOrderMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tblOrder.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih item yang ingin dihapus dulu yaaa!");
            return;
        }

        orderList.remove(row);

        refreshOrderTable();

        txtNama.setText("");
        txtJumlah.setText("");

        JOptionPane.showMessageDialog(this, "Item sudah berhasil dihapus Yaaa!!!");
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbah1ActionPerformed

        if (selectedOrderIndex == -1) {
            JOptionPane.showMessageDialog(this, "Sebelum itu Pilih item yang ingin diubah dulu yaaa!");
            return;
        }

        try {
            int jumlahBaru = Integer.parseInt(txtJumlah.getText().trim());

            if (jumlahBaru <= 0) {
                JOptionPane.showMessageDialog(this, "Humm Jumlah harus lebih dari 0!");
                return;
            }

            // Ambil item lama dari orderList
            OrderItem item = orderList.get(selectedOrderIndex);

            // Ubah jumlahnya
            OrderItem updatedItem = new OrderItem(item.getMenu(), jumlahBaru);
            orderList.set(selectedOrderIndex, updatedItem);

            // Refresh tabel (dan total)
            refreshOrderTable();
            hitungTotalHarga(); // method yang kita tambahkan di bawah

            JOptionPane.showMessageDialog(this, "Yess Item berhasil diubah!");

            selectedOrderIndex = -1;

            txtNama.setText("");
            txtJumlah.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Maaf Jumlah harus berupa angka!");
        }


    }//GEN-LAST:event_btnUbah1ActionPerformed

