    private void loadQR() {
        try {
            ImageIcon icon = new ImageIcon(getClass().getResource("/images/qris.jpg"));

            Image img = icon.getImage().getScaledInstance(
                    lblQris.getWidth(),
                    lblQris.getHeight(),
                    Image.SCALE_SMOOTH
            );

            lblQris.setIcon(new ImageIcon(img));

        } catch (Exception e) {
            System.out.println("Yahh Gagal memuat QR code!");
            e.printStackTrace();
        }
    }

